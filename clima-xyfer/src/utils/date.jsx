export const MONTHS = [
  "Enero",
  "Febrero",
  "Marzo",
  "Abril",
  "Mayo",
  "Junio",
  "Julio",
  "Agosto",
  "Septiembre",
  "Octubre",
  "Noviembre",
  "Diciembre",
];

export const DAYS = [
  "Lunes",
  "Martes",
  "Miércoles",
  "Jueves",
  "Viernes",
  "Sábado",
  "Domingo",
];

export function getCurrentDate() {
  const current = new Date();
  let day = current.getDate();
  let month = current.getMonth();
  return `${[DAYS[day]]}, ${day} ${MONTHS[month]} `;
}
